#import "UIColor+SugarColors.h"

@implementation UIColor (SugarColors)

+ (UIColor *)SUGWhiteColor
{
    return [UIColor colorWithRed:1.0
                           green:1.0
                            blue:1.0
                           alpha:1.0];
}
+ (UIColor *)SUGBlueBackgroundColor
{
    return [UIColor colorWithRed:0.84
                           green:0.88
                            blue:0.90
                           alpha:1.0];
}
+ (UIColor *)SUGBlueHighlightColor
{
    return [UIColor colorWithRed:0.0
                           green:0.64
                            blue:0.93
                           alpha:1.0];
}

+ (UIColor *)SUGNavyBlueBackgroundColor
{
    return [UIColor colorWithRed:0.11
                           green:0.15
                            blue:0.19
                           alpha:1.0];
}



@end
